texto = "Python é divertido!"

# Fatiando do início até o índice 6 (não inclui o 6)
print(texto[:6])         # Python

# Fatiando do índice 7 até o final
print(texto[7:])         # é divertido!

# Fatiando do índice 7 ao 9 (não inclui o 9)
print(texto[7:9])        # é 

# Fatiando com passo
print(texto[::2])        # Pto édvri!

# Fatiando os 7 últimos caracteres
print(texto[-7:])
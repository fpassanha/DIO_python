saldo = 500

print(f"Saldo inicial: {saldo}")

saldo += 100  # Adiciona 100 ao saldo
print(f"Saldo após depósito: {saldo}")  

saldo -= 50   # Subtrai 50 do saldo
print(f"Saldo após saque: {saldo}") 

saldo *= 2   # Multiplica o saldo por 2
print(f"Saldo após multiplicação: {saldo}")

saldo /= 4   # Divide o saldo por 4
print(f"Saldo após divisão: {saldo}")   

saldo %= 3   # Calcula o resto da divisão do saldo por 3
print(f"Saldo após módulo: {saldo}")

saldo **= 2  # Eleva o saldo ao quadrado
print(f"Saldo após exponenciação: {saldo}") 



